def _chunk(items: list[str], sizes: list[int]) -> list[list[str]]:
    out: list[list[str]] = []
    idx = 0
    for size in sizes:
        out.append(items[idx : idx + size])
        idx += size
    return out


def _build_section_blocks(topic: str, focus: str, operation: str) -> list[str]:
    blocks: list[str] = []
    for i, (title, axis, action) in enumerate(SECTION_BLUEPRINTS, start=1):
        blocks.append(
            dedent(
                f"""
                【実践セクション{i:02d}】{title}
                {topic}では「{axis}」を先に固定すると、学習判断がぶれにくくなります。
                賃貸不動産経営管理士試験は3分野50問120分で構成されるため、知識学習と時間運用を同時に整えることが重要です。
                とくに{focus}で迷った場面では、試験センター公表情報と教材の記述差分を確認して判断してください。
                実行段階では、{action}を小さく回し、{operation}を毎週更新する流れが有効です。
                """
            ).strip()
        )
    return blocks


def _build_faq_blocks(topic: str) -> list[str]:
    blocks: list[str] = []
    for i, (question, answer) in enumerate(FAQ_BLUEPRINTS, start=1):
        blocks.append(
            f"Q{i:02d}. {question}\n"
            f"A{i:02d}. {topic}では、{answer} "
            "迷った時は3分野のどこで失点したかを先に切り分けると、対策の優先順位を決めやすくなります。"
        )
    return blocks


def _compose_entry(
    *,
    topic: str,
    focus: str,
    operation: str,
    meta_description: str,
    user_intent: str,
    action_items: list[str],
) -> dict[str, str]:
    section_groups = _chunk(_build_section_blocks(topic, focus, operation), SECTION_GROUP_SIZES)
    faq_groups = _chunk(_build_faq_blocks(topic), FAQ_GROUP_SIZES)

    entry: dict[str, str] = {
        "meta_description": meta_description,
        "user_intent": user_intent,
        "action_items": ";".join(action_items),
        "section_1_heading": "公式情報と前提の固定",
        "section_2_heading": "教材と演習の設計",
        "section_3_heading": "時間配分と解答順",
        "section_4_heading": "誤答分析と復習運用",
        "section_5_heading": "通信講座と独学の判断",
        "section_6_heading": "直前期と当日準備",
        "section_7_heading": "再受験を含む継続改善",
        "faq_1_question": "最初の一歩は何ですか？",
        "faq_2_question": "教材運用で失敗しない方法は？",
        "faq_3_question": "時間管理の改善ポイントは？",
    }
    for i, group in enumerate(section_groups, start=1):
        entry[f"section_{i}_body"] = "\n\n".join(group)
    for i, group in enumerate(faq_groups, start=1):
        entry[f"faq_{i}_answer"] = "\n\n".join(group)
    return entry


def _slug_is_hand_done(slug: str) -> bool:
    csv_path = Path(__file__).resolve().parents[1] / "data" / "guide_articles.csv"
    if not csv_path.is_file():
        return False
    with csv_path.open(encoding="utf-8-sig") as f:
        for row in csv.DictReader(f):
            if (row.get("slug") or "").strip() != slug:
                continue
            note = (row.get("revision_note") or "").strip()
            return any(marker in note for marker in HAND_MARKERS)
    return False


