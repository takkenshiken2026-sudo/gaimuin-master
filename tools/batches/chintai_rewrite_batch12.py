def _chunk(items: list[str], sizes: list[int]) -> list[list[str]]:
    out: list[list[str]] = []
    idx = 0
    for size in sizes:
        out.append(items[idx : idx + size])
        idx += size
    return out


def _build_section_blocks(topic: str, focus: str) -> list[str]:
    blocks: list[str] = []
    for i, (title, keyword, action) in enumerate(SECTION_BLUEPRINTS, start=1):
        blocks.append(
            dedent(
                f"""
                【拡張セクション{i:02d}】{title}
                {topic}の学習では「{keyword}」を起点に判断基準をそろえると、設問処理が安定します。
                とくに{focus}に関する設問は、賃貸不動産経営管理士 examの文脈で主体・期限・条件が同時に問われます。
                実行手順としては、{action}を固定し、学習ログへ翌週の修正点を一行で残してください。
                迷った項目は試験センターの公表情報と照合し、3分野のどこに属するかを再分類すると再発を減らせます。
                """
            ).strip()
        )
    return blocks


def _build_faq_blocks(topic: str) -> list[str]:
    blocks: list[str] = []
    for i, (q, a) in enumerate(FAQ_BLUEPRINTS, start=1):
        blocks.append(
            f"Q{i:02d}. {q}\n"
            f"A{i:02d}. {topic}の対策では、{a} "
            "判断に迷う場合は試験センターの最新案内を確認し、3分野の学習計画へ反映してください。"
        )
    return blocks


def _compose_entry(
    *,
    title: str,
    focus: str,
    meta: str,
    intent: str,
    actions: list[str],
    faq_title: str,
) -> dict[str, str]:
    section_blocks = _build_section_blocks(title, focus)
    faq_blocks = _build_faq_blocks(faq_title)

    section_groups = _chunk(section_blocks, [6, 5, 5, 5, 5, 5, 5])
    faq_groups = _chunk(faq_blocks, [7, 7, 7])

    entry: dict[str, str] = {
        "meta_description": meta,
        "user_intent": intent,
        "action_items": ";".join(actions),
        "section_1_heading": "方針の確認",
        "section_2_heading": "準備の手順",
        "section_3_heading": "演習の進め方",
        "section_4_heading": "復習の設計",
        "section_5_heading": "直前期の調整",
        "section_6_heading": "本番対応の確認",
        "section_7_heading": "運用チェックリスト",
        "faq_1_question": f"{faq_title}は何から始めるべきですか？",
        "faq_2_question": f"{faq_title}で失点を減らすコツは？",
        "faq_3_question": f"{faq_title}を独学で継続する方法は？",
    }

    for i, group in enumerate(section_groups, start=1):
        entry[f"section_{i}_body"] = "\n\n".join(group)
    for i, group in enumerate(faq_groups, start=1):
        entry[f"faq_{i}_answer"] = "\n\n".join(group)
    return entry


