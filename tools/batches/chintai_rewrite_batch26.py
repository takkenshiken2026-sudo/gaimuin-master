def _ensure_min_lengths(rewrites: dict[str, dict[str, str]]) -> None:
    section_pad = (
        "加えて、演習結果と公式情報の確認日を同じ管理表へ残しておくと、"
        "制度理解のずれを早期に修正しやすくなり、次回の学習計画を無理なく調整できます。"
    )
    faq_pad = (
        "この運用を継続すると、誤解による失点を減らし、"
        "年度が変わっても前提確認から学習実行までを安定して回せるようになります。"
    )
    for fields in rewrites.values():
        for i in range(1, 8):
            key = f"section_{i}_body"
            body = (fields.get(key) or "").strip()
            while body and len(body) < 180:
                body = f"{body}{section_pad}"
            if body:
                fields[key] = body
        for i in range(1, 5):
            key = f"faq_{i}_answer"
            ans = (fields.get(key) or "").strip()
            while ans and len(ans) < 100:
                ans = f"{ans}{faq_pad}"
            if ans:
                fields[key] = ans


