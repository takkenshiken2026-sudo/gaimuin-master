def _chunk(items: list[str], sizes: list[int]) -> list[list[str]]:
    out: list[list[str]] = []
    idx = 0
    for size in sizes:
        out.append(items[idx : idx + size])
        idx += size
    return out


def _build_section_blocks(topic: str, focus: str) -> list[str]:
    blocks: list[str] = []
    for i, (title, keyword, action) in enumerate(SECTION_BLUEPRINTS, start=1):
        blocks.append(
            dedent(
                f"""
                【実践セクション{i:02d}】{title}
                {topic}では「{keyword}」を判断軸として先に固定すると、日々の学習がぶれにくくなります。
                賃貸不動産経営管理士の学習は、3分野を往復しながら精度を高める設計が有効です。
                とくに{focus}に迷った時は、試験センターの公表情報を確認し、一次情報を優先して修正してください。
                実行段階では、{action}を小さく回し、週次レビューで改善点を1つだけ更新する運用が効果的です。
                """
            ).strip()
        )
    return blocks


def _build_faq_blocks(topic: str) -> list[str]:
    blocks: list[str] = []
    for i, (q, a) in enumerate(FAQ_BLUEPRINTS, start=1):
        blocks.append(
            f"Q{i:02d}. {q}\n"
            f"A{i:02d}. {topic}では、{a} "
            "迷う論点は3分野のどこに属するかを確認し、学習記録へ反映すると改善が早まります。"
        )
    return blocks


def _compose_entry(
    *,
    title: str,
    focus: str,
    meta: str,
    intent: str,
    actions: list[str],
    faq_title: str,
) -> dict[str, str]:
    section_blocks = _build_section_blocks(title, focus)
    faq_blocks = _build_faq_blocks(faq_title)

    section_groups = _chunk(section_blocks, [6, 5, 5, 5, 5, 5, 5])
    faq_groups = _chunk(faq_blocks, [7, 7, 7])

    entry: dict[str, str] = {
        "meta_description": meta,
        "user_intent": intent,
        "action_items": ";".join(actions),
        "section_1_heading": "独学前に公式情報を確認",
        "section_2_heading": "教材・参考書の選び方",
        "section_3_heading": "過去問で現在地を確認",
        "section_4_heading": "復習を計画に入れる",
        "section_5_heading": "直前期の絞り込み",
        "section_6_heading": "当日対応の準備",
        "section_7_heading": "継続運用の見直し",
        "faq_1_question": "独学では何から始めればよいですか？",
        "faq_2_question": "過去問だけで足りますか？",
        "faq_3_question": "独学でのおすすめの進め方は？",
    }

    for i, group in enumerate(section_groups, start=1):
        entry[f"section_{i}_body"] = "\n\n".join(group)
    for i, group in enumerate(faq_groups, start=1):
        entry[f"faq_{i}_answer"] = "\n\n".join(group)
    return entry


