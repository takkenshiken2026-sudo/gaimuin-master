def _chunk(items: list[str], sizes: list[int]) -> list[list[str]]:
    out: list[list[str]] = []
    idx = 0
    for size in sizes:
        out.append(items[idx : idx + size])
        idx += size
    return out


def _build_section_blocks(topic: str, focus: str) -> list[str]:
    blocks: list[str] = []
    for i, (title, keyword, action) in enumerate(SECTION_BLUEPRINTS, start=1):
        blocks.append(
            dedent(
                f"""
                【実践セクション{i:02d}】{title}
                {topic}では「{keyword}」を先に定義しておくと、演習と復習の判断がぶれにくくなります。
                賃管 examは3分野を横断して50q/120minで処理するため、知識量だけでなく運用手順の固定が重要です。
                とくに{focus}で迷った場面では、公式発表を確認して一次情報ベースで手順を更新してください。
                実行段階では、{action}を週次で反復し、再発誤答の減少を改善指標に置くと安定します。
                """
            ).strip()
        )
    return blocks


def _build_faq_blocks(topic: str) -> list[str]:
    blocks: list[str] = []
    for i, (q, a) in enumerate(FAQ_BLUEPRINTS, start=1):
        blocks.append(
            f"Q{i:02d}. {q}\n"
            f"A{i:02d}. {topic}では、{a} "
            "迷う論点は3分野のどこに属するかを明確にし、次回演習の開始前に確認すると改善が早まります。"
        )
    return blocks


def _compose_entry(
    *,
    title: str,
    focus: str,
    meta: str,
    intent: str,
    actions: list[str],
    faq_title: str,
) -> dict[str, str]:
    section_blocks = _build_section_blocks(title, focus)
    faq_blocks = _build_faq_blocks(faq_title)

    section_groups = _chunk(section_blocks, [6, 5, 5, 5, 5, 5, 5])
    faq_groups = _chunk(faq_blocks, [7, 7, 7])

    entry: dict[str, str] = {
        "meta_description": meta,
        "user_intent": intent,
        "action_items": ";".join(actions),
        "section_1_heading": "公式情報と分析軸を固定",
        "section_2_heading": "点数分析の記録手順",
        "section_3_heading": "誤答原因の分解",
        "section_4_heading": "最新年度の使い方",
        "section_5_heading": "時間計測演習の設計",
        "section_6_heading": "直前期の優先順位",
        "section_7_heading": "本番運用と振り返り",
        "faq_1_question": "過去問活用の最初の一手は？",
        "faq_2_question": "時間不足と知識不足の見分け方は？",
        "faq_3_question": "最新年度問題の扱い方は？",
    }

    for i, group in enumerate(section_groups, start=1):
        entry[f"section_{i}_body"] = "\n\n".join(group)
    for i, group in enumerate(faq_groups, start=1):
        entry[f"faq_{i}_answer"] = "\n\n".join(group)
    return entry


